# Define Variables
# Define the 'x' variable and give it a value
x = "There are #{10} types of people."

# Define the 'binary' variable and give it a value
binary = "binary"

# Define the 'do_not' variable and give it a value
do_not = "don't"

# Define the 'y' variable and give it a value
y = "Those who know #{binary} and those who #{do_not}."

# Print x and y variables
puts x
puts y

# Prints string with 'x' variable
puts "I said: #{x}."

# Prints string with 'x' variable
puts "I also said: '#{y}'."

# Defines variable 'hilarious' and gives it a value of 'false'
hilarious = false

# Defines variable 'joke_evaluation' and gives it a value
joke_evaluation = "Isn't that joke so funny?! #{hilarious}"

# Prints 'joke_evaluation'
puts joke_evaluation

